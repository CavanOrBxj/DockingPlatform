        private static int FindItem(ushort[] ushortArray, int key)
        {
            int low = 0;
            int mid;
            int high = ushortArray.Length - 1;
            while (low <= high)
            {
                if ((low + high) % 2 == 0)
                    mid = (low + high) / 2;
                else
                    mid = (low + high) / 2 + 1;
                if (ushortArray[mid] == key)
                    return mid;
                else if (ushortArray[mid] > key)
                    high = mid - 1;
                else
                    low = mid + 1;
            }
            return 0;
        }
