        public static String EncryptBase64Code(String text)
        {
            Byte[] bufin = System.Text.ASCIIEncoding.Default.GetBytes(text);
            String result = Convert.ToBase64String(bufin, 0, bufin.Length);
            return result;
        }

        public static String DecryptBase64Code(String text)
        {
            try
            {
                Byte[] bufout = Convert.FromBase64String(text);
                String result = System.Text.ASCIIEncoding.Default.GetString(bufout);
                return result;
            }
            catch
            {
                return text;
            }
        } 

        public static string GetPinYinCode(string sourceString)
        {
            int j = 1;
            string result = "";
            while (j <= sourceString.Length)
            {
                char a = sourceString[j - 1];
                if (ChineseChar.IsValidChar(a))
                    result = result + GetPinYinIndexChar(a.ToString());
                else
                    result = result + a.ToString();
                j++;

            }
            return result;

        }


        private static string GetPinYinIndexChar(string p)
        {
            ChineseChar c = new ChineseChar(p[0]);
            string result = c.Pinyins[0][0].ToString();
            return result;
        }
